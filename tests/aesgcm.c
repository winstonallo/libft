// AES-GCM Test Vectors (13-18 for AES256-GCM - only 13-16 are tested since 17 and 18 test for
// IV sizes other than 96, which this implementation does not support).
// https://luca-giuzzi.unibs.it/corsi/Support/papers-cryptography/gcm-spec.pdf

bool
test_vector_13() {
    uint8_t K[32] = {0};

    Aes256Gcm X = {0};
    AES256_Init((Aes256Data *)&X, K, NULL, 0); // 0 length
    uint8_t iv[12] = {0};
    ft_memcpy(X.iv, iv, 12);
    GCM_AE(&X, &X);

    const uint8_t T[16] = {0x53, 0x0f, 0x8a, 0xfb, 0xc7, 0x45, 0x36, 0xb9, 0xa9, 0x63, 0xb4, 0xf1, 0xc4, 0xcb, 0x73, 0x8b};
    if (ft_memcmp(T, X.tag, TAG_LEN_BYTES)) {
        return false;
    }
    if (GCM_AD(&X, &X) == -1) {
        return false;
    }

    return true;
}

