uint8_t *
Cipher(const uint8_t *in, uint8_t *const out, uint32_t *w) {
    uint8_t *const state = out;
    __CopyState((uint8_t *)in, out);

    AddRoundKey(state, w);

    for (int round = 1; round < Nr; ++round) {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, &w[4 * round]);
    }

    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, &w[4 * Nr]);

    return state;
}

uint32_t *
KeyExpansion(const uint8_t key[32], uint32_t *const out) {
    uint32_t *const w = out;

    for (int i = 0; i < Nk; ++i) {
        w[i] = ((uint32_t)key[4 * i] << 24) | ((uint32_t)key[4 * i + 1] << 16) | ((uint32_t)key[4 * i + 2] << 8) | ((uint32_t)key[4 * i + 3]);
    }

    for (int i = Nk; i <= 4 * Nr + 3; ++i) {
        uint32_t temp = w[i - 1];
        if (i % Nk == 0) {
            temp = SubWord(RotWord(temp)) ^ Rcon[i / Nk];
        } else if (Nk > 6 && i % Nk == 4) {
            temp = SubWord(temp);
        }
        w[i] = w[i - Nk] ^ temp;
    }
    return w;
}

uint8_t *
InvCipher(uint8_t *in, uint8_t *const out, uint32_t *w) {
    uint8_t *const state = out;
    __CopyState((uint8_t *)in, out);

    AddRoundKey(state, &w[4 * Nr]);

    for (int round = Nr - 1; round >= 1; --round) {
        InvShiftRows(state);
        InvSubBytes(state);
        AddRoundKey(state, &w[4 * round]);
        InvMixColumns(state);
    }

    InvShiftRows(state);
    InvSubBytes(state);
    AddRoundKey(state, &w[0]);

    return state;
}

void
AES256_Init(Aes256Data *data, const uint8_t key[AES256_KEY_SIZE_BYTES], const uint8_t *const msg, const size_t msg_len) {
    KeyExpansion(key, data->expanded_key);
    ft_memcpy(data->key, key, AES256_KEY_SIZE_BYTES);
    data->msg.data = (uint8_t *)msg;
    data->msg.len = msg_len;
}
