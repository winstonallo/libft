#define InvSBox(b) InvSBoxArray[(b & 0xF0) >> 4][b & 0x0F]
#define SBox(b) SBoxArray[(b & 0xF0) >> 4][b & 0x0F]

#define Mul02(b) xTimes(b)
#define Mul03(b) (xTimes(b) ^ (b))
#define Mul09(b) (xTimes(xTimes(xTimes(b))) ^ (b))
#define Mul0b(b) (xTimes(xTimes(xTimes(b))) ^ xTimes(b) ^ (b))
#define Mul0d(b) (xTimes(xTimes(xTimes(b))) ^ xTimes(xTimes(b)) ^ (b))
#define Mul0e(b) (xTimes(xTimes(xTimes(b))) ^ xTimes(xTimes(b)) ^ xTimes(b))

static const uint32_t Rcon[11] = {
    0x00000000, 0x01000000, 0x02000000, 0x04000000, 0x08000000, 0x10000000, 0x20000000, 0x40000000, 0x80000000, 0x1b000000, 0x36000000,
};

__attribute__((always_inline)) static inline uint32_t
RotWord(const uint32_t val) {
    return (val << 8) | (val >> 24);
}

__attribute__((always_inline)) static inline uint32_t
SubWord(const uint32_t val) {
    return (uint32_t)((SBox(val & 0xFF)) | (SBox(((val >> 8) & 0xFF)) << 8) | (SBox(((val >> 16) & 0xFF)) << 16) | (SBox(((val >> 24) & 0xFF)) << 24));
}

__attribute__((always_inline)) static inline void
SubBytes(uint8_t state[16]) {
    for (int i = 0; i < 16; ++i) {
        state[i] = SBox(state[i]);
    }
}

__attribute__((always_inline)) static inline void
InvSubBytes(uint8_t state[16]) {
    for (int i = 0; i < 16; ++i) {
        state[i] = InvSBox(state[i]);
    }
}

__attribute__((always_inline)) static inline uint8_t
xTimes(uint8_t b) {
    if ((b & 0x80) == 0) {
        return b << 1;
    }
    return (b << 1) ^ 0x1B;
}

__attribute__((always_inline)) static inline void
ShiftRows(uint8_t state[16]) {
    uint8_t temp = state[1];
    state[1] = state[5];
    state[5] = state[9];
    state[9] = state[13];
    state[13] = temp;

    temp = state[2];
    state[2] = state[10];
    state[10] = temp;
    temp = state[6];
    state[6] = state[14];
    state[14] = temp;

    temp = state[15];
    state[15] = state[11];
    state[11] = state[7];
    state[7] = state[3];
    state[3] = temp;
}

__attribute__((always_inline)) static inline void
InvShiftRows(uint8_t state[16]) {
    uint8_t temp = state[13];
    state[13] = state[9];
    state[9] = state[5];
    state[5] = state[1];
    state[1] = temp;

    temp = state[2];
    state[2] = state[10];
    state[10] = temp;
    temp = state[6];
    state[6] = state[14];
    state[14] = temp;

    temp = state[3];
    state[3] = state[7];
    state[7] = state[11];
    state[11] = state[15];
    state[15] = temp;
}

__attribute__((always_inline)) static inline void
MixColumns(uint8_t state[16]) {
    for (int c = 0; c < 4; c++) {
        uint8_t s0 = state[c * 4 + 0];
        uint8_t s1 = state[c * 4 + 1];
        uint8_t s2 = state[c * 4 + 2];
        uint8_t s3 = state[c * 4 + 3];

        state[c * 4 + 0] = Mul02(s0) ^ Mul03(s1) ^ s2 ^ s3;
        state[c * 4 + 1] = s0 ^ Mul02(s1) ^ Mul03(s2) ^ s3;
        state[c * 4 + 2] = s0 ^ s1 ^ Mul02(s2) ^ Mul03(s3);
        state[c * 4 + 3] = Mul03(s0) ^ s1 ^ s2 ^ Mul02(s3);
    }
}

__attribute__((always_inline)) static inline void
InvMixColumns(uint8_t state[16]) {
    for (int c = 0; c < 4; c++) {
        uint8_t s0 = state[c * 4 + 0];
        uint8_t s1 = state[c * 4 + 1];
        uint8_t s2 = state[c * 4 + 2];
        uint8_t s3 = state[c * 4 + 3];

        state[c * 4 + 0] = Mul0e(s0) ^ Mul0b(s1) ^ Mul0d(s2) ^ Mul09(s3);
        state[c * 4 + 1] = Mul09(s0) ^ Mul0e(s1) ^ Mul0b(s2) ^ Mul0d(s3);
        state[c * 4 + 2] = Mul0d(s0) ^ Mul09(s1) ^ Mul0e(s2) ^ Mul0b(s3);
        state[c * 4 + 3] = Mul0b(s0) ^ Mul0d(s1) ^ Mul09(s2) ^ Mul0e(s3);
    }
}

__attribute__((always_inline)) static inline void
AddRoundKey(uint8_t state[16], uint32_t *w) {
    for (int c = 0; c < 4; c++) {
        uint32_t word = w[c];
        state[c * 4 + 0] ^= (word >> 24) & 0xFF;
        state[c * 4 + 1] ^= (word >> 16) & 0xFF;
        state[c * 4 + 2] ^= (word >> 8) & 0xFF;
        state[c * 4 + 3] ^= word & 0xFF;
    }
}

